// Dynamic alerts based on actual location, hospitals, and required specialist
export const generateAlerts = (locationName, dynamicHospitals, requiredSpecialist, ambulanceCoords) => {
  const alertsList = [];
  let id = 1;

  // Inline distance helper (avoids ordering issue with const)
  const distKm = (lat1, lon1, lat2, lon2) => {
    const R = 6371;
    const dLat = (lat2 - lat1) * (Math.PI / 180);
    const dLon = (lon2 - lon1) * (Math.PI / 180);
    const a = Math.sin(dLat/2)**2 + Math.cos(lat1*(Math.PI/180)) * Math.cos(lat2*(Math.PI/180)) * Math.sin(dLon/2)**2;
    return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
  };

  // Alert 1: Critical patient inbound from current location
  const locLabel = locationName === 'Current GPS Location' ? 'current GPS area' : locationName;
  alertsList.push({ id: id++, type: 'critical', message: `Critical patient detected - inbound from ${locLabel}` });

  // Alert 2: Check specialist availability within 10 km
  if (requiredSpecialist) {
    const nearbyWithSpecialist = dynamicHospitals.filter(h => {
      return distKm(ambulanceCoords[0], ambulanceCoords[1], h.lat, h.lng) <= 10 && h.specialists.includes(requiredSpecialist);
    });
    if (nearbyWithSpecialist.length === 0) {
      alertsList.push({ id: id++, type: 'warning', message: `No ${requiredSpecialist}s available within 10 km` });
    }
  }

  // Alert 3: Any overloaded hospitals nearby
  const overloaded = dynamicHospitals.filter(h => h.occupancy_rate > 90);
  if (overloaded.length > 0) {
    const worst = overloaded.sort((a, b) => b.occupancy_rate - a.occupancy_rate)[0];
    alertsList.push({ id: id++, type: 'danger', message: `${worst.hospital_name} overloaded (${worst.occupancy_rate}% occupancy)` });
  }

  // Alert 4: Low trauma bed warning
  const lowTrauma = dynamicHospitals.filter(h => {
    return distKm(ambulanceCoords[0], ambulanceCoords[1], h.lat, h.lng) <= 15 && h.trauma_beds <= 1 && h.status !== 'Overloaded';
  });
  if (lowTrauma.length >= 3) {
    alertsList.push({ id: id++, type: 'warning', message: `${lowTrauma.length} nearby hospitals have critically low trauma beds` });
  }

  return alertsList;
};

