// Helper to determine severity based on vitals
export const calculateSeverity = (vitals) => {
  const { heartRate, bloodPressure, spO2, bleeding, consciousness } = vitals;
  
  if (spO2 < 90 || consciousness === 'Unconscious' || bleeding === 'Severe' || heartRate > 130 || heartRate < 40) {
    return { level: 'Critical', color: 'danger', reason: 'Unstable vitals, critical risk to life.' };
  }
  if (spO2 < 95 || bleeding === 'Moderate' || heartRate > 100 || consciousness === 'Confused') {
    return { level: 'High Risk', color: 'warning', reason: 'Vitals concerning, immediate care required.' };
  }
  return { level: 'Stable', color: 'success', reason: 'Vitals stable, standard monitoring.' };
};

// Helper to calculate distance in km between two lat/lng points
const getDistanceFromLatLonInKm = (lat1, lon1, lat2, lon2) => {
  const R = 6371; // Radius of the earth in km
  const dLat = (lat2 - lat1) * (Math.PI / 180);  // deg2rad below
  const dLon = (lon2 - lon1) * (Math.PI / 180); 
  const a = 
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos(lat1 * (Math.PI / 180)) * Math.cos(lat2 * (Math.PI / 180)) * 
    Math.sin(dLon / 2) * Math.sin(dLon / 2); 
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a)); 
  const d = R * c; // Distance in km
  return d;
};

// Traffic condition simulation — deterministic (no random jitter)
const getTrafficCondition = (distanceKm) => {
  const hour = new Date().getHours();
  const isPeak = (hour >= 8 && hour <= 10) || (hour >= 17 && hour <= 20);
  const baseMultiplier = isPeak ? 3.0 : 2.2;
  // Deterministic jitter from distance so same hospital = same result
  const stableHash = ((distanceKm * 1000) % 100) / 250; // 0.00 to 0.40
  const multiplier = baseMultiplier + stableHash;
  const eta = Math.max(2, Math.round(distanceKm * multiplier));
  const label = multiplier > 3.0 ? 'Heavy Traffic' : (multiplier > 2.5 ? 'Moderate' : 'Normal');
  return { eta, label, multiplier };
};

